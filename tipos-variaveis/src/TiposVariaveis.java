public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        // tipos de variaveis da linguagem java
        String primeiroNome = "Lucas"; //variável para cadeias de caracteres

        byte idade = 24; // variavel para numeros que recebe valores que vão de -128 a 127

        float altura = 1.70f; //variavel que recebe números com ponto flutuante, deve terminar com a letra f;

        short anoNascimento = 1999; // variavel para numeros que recebe valores que vão de -32k+- até +-32k

        double dinheiro = 1000.50; // variavel para numeros com ponto flutuante

        long bilhao = 1000000000l; // variavel para números muito grandes, deve terminar com a letra l;

        final String sobrenome = "Vieira"; //para declarar uma constante, devo utilizar o escopo = final tipoVariavel nomeVariavel atribuição
        
        boolean verdade = true; //variavel lógica que recebe valores true ou false 
    }
}