public class Operadores {
    public static void main(String[] args) {

        double soma = 10.5 + 15.7; // operador soma " + "
        int subtracao = 15 - 5; //operador subtração " - "
        int multiplicacao = 15 * 3; //operador multiplicação " * "
        int divisao = 15 / 5 ; // operador divisão " / "
        int modulo = 18 % 3; // operador módulo(resto da divisão) " % "
        boolean verdade = true;
        int numero = 15;
        //numero++; //numero + 1

        System.out.println(++ numero); // ++ operador para incremento de valor (adiciona 1 ao valor)
        System.out.println(-- numero); // -- operador para decremento de valor (diminue 1 ao valor)
        System.out.println(!verdade); // ! operador que nega o valor da variavel(inverte boolean)
     }
}
